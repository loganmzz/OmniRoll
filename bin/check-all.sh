#!/usr/bin/env bash
set -o pipefail

bindir="$(cd "$(dirname "${BASH_SOURCE}")" >/dev/null; pwd)" &&
source "${bindir}/__init.shrc" &&
true || exit $?

function main.run() {
  echo
  echo '################################################################################'
  echo '################################# Check Schemas ################################'
  echo '################################################################################'
  "${bindir}/check-schemas.sh" || return $?

  echo
  echo '################################################################################'
  echo '################################ Check Examples ################################'
  echo '################################################################################'
  "${bindir}/check-examples.sh" || return $?

  echo
  echo '################################################################################'
  echo '################################### Check Data #################################'
  echo '################################################################################'
  "${bindir}/check-data.sh" || return $?

  echo
  echo '################################################################################'
  echo '################################### Check Test #################################'
  echo '################################################################################'
  "${bindir}/check-test.sh" || return $?

  echo
  echo '################################################################################'
  echo '################################### Check Lint #################################'
  echo '################################################################################'
  "${bindir}/check-lint.sh" || return $?

  echo
  echo '################################################################################'
  return 0
}

main "$@"
